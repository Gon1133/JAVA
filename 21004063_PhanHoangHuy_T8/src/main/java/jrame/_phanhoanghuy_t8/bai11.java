    private void btn_scMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_scMouseClicked
        // TODO add your handling code here:
        try{
            // bỏ tô đen
            list_ptu.clearSelection();
            for (int i=0; i<arrInt.size(); i++){
                if (arrInt.get(i) % 2 == 0) {
                    // tô đen các ptu JList tại vị trí i
                    list_ptu.addSelectionInterval(i,i);
                }
            }
        }
        catch(Exception ex){
            JOptionPane.showMessageDialog(rootPane, "Da co loi !");
        }
    }//GEN-LAST:event_btn_scMouseClicked

