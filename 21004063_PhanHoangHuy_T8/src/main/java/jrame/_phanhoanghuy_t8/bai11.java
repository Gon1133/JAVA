    private void btn_xoaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_xoaMouseClicked
        // TODO add your handling code here:
        if (list_ptu.getSelectedIndex() != -1){
            int[] a = list_ptu.getSelectedIndices();
            // Sắp xếp mảng các vị trí giảm dần để xóa từ cuối lên đầu
            Arrays.sort(a);
            // Xóa các phần tử được chọn từ cuối lên đầu
            for (int i = a.length-1; i>=0; i--)
                arrInt.remove(a[i]);
            CapnhatList();
        }
        else 
            JOptionPane.showMessageDialog(rootPane, "Hãy chọn giá trị muốn xóa !");
        
    }//GEN-LAST:event_btn_xoaMouseClicked

    private void btn_tongMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_tongMouseClicked
        // TODO add your handling code here:
        if (arrInt.isEmpty())
            JOptionPane.showMessageDialog(rootPane, "Không có phần tử trong list");
        else{
            int tong=0;
            for (int i: arrInt)
                tong += i;
            JOptionPane.showMessageDialog(rootPane, "Tổng giá trị phần tử trong List = "+tong);
        }
    }//GEN-LAST:event_btn_tongMouseClicked

